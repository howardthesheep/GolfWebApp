	      	<p>The 2nd hole is a longer par 3 playing through the valley along side Grindstone Creek.  This par 3, which plays to a length of 215 yards from the tournament tees, is level from tee to green.  A large meandering bunker guards the front left-hand portion of the green.  The green contours set up a variety of hole locations and give the player the option of trying to carry the bunker to get closer to left-hand pin positions or running a shot up onto the green to a variety of hole locations on the right-hand side.  The green is wrapped with fairway on three sides (front, back, and right) to create options of play for the player that has missed the green surface with their tee shot.</p>

