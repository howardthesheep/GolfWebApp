	      	<p>The 14th hole is a mid-length par 4 doglegging sharply right.  Three small bunkers are set into a natural ridge running through the landing area, creating two distinct options for players off the tee.  Playing into the lower right-hand fairway is risky, but shortens the hole substantially.  The upper left-hand fairway is large and accommodating, but the angle of approach will bring the greenside bunker into play, while also lengthening the hole.  As is the theme throughout the golf course, players are given options of play throughout.</p>

