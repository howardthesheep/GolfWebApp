	      	<p>Coming off the longest hole on the golf course, golfers will now encounter the longest par 3 on the course.  The 7th hole plays to a length of 258 yards from the tournament tees and will require everyone to hit their best tee shot here if they hope to make par.  There are no bunkers on this hole and no hazards are in play either, but the hole will still require the most out of every player.  The large green is set into a slight hillside which creates some support on the right and back side of the green.  Some slight undulations within the green help to define a variety of hole locations and there is plenty of fairway front and left of the green to allow players to run a shot up onto the green.</p>

