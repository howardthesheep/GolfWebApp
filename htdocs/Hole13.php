	      	<p>The 13th hole is one of the longest par 4's on the golf course, playing to a length of 450 yards from the tournament tees.  It sits perfectly within the existing lay of the land.  The tees play from an elevated ridge down into a broad low.  The second shot is played from this low up to a green setting on top of the next ridge.  The location of the tee shot in the fairway will be of critical importance to the golfer if they want to see the green surface at all.  A couple of bunkers along the right-hand side of the fairway help to define the strategy off the tee, as a shot closer to the bunker will provide the player with more visibility to the green surface for their approach shot.  The green itself is somewhat of a bowl with balls feeding onto the green from the hill slope in front.  The green is completely ringed with fairway and is quite large in size.</p>

