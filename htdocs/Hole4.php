	      	<p>The fourth hole is a strong par 4 playing 440 yards from the tournament tees, generally uphill from tee to green.  Off the tee the players can only see the ridge that crosses the fairway just short of the landing area.  A well played shot should carry the ridge and give the player a little additional distance as it hits the down slope and bounces forward.  Short of the ridge the fairway moves hard from right to left and the creek that runs down the left-hand side of the fairway should give a player reason to pause and consider where they want their shot to end up off the tee.  Once over the ridge, the rest of the golf hole opens to a wonderful green setting nestled amongst a stand of trees with a small creek dancing across the front of the green. Players will be confronted with a decision on their second shot, depending on how well they have hit their tee shot, as to whether to go for the green, play short of the creek or play left of the green into a large fairway bailout area that shortens the carry over the creek but leaves them off the green with their third shot.  The large green has four or five distinct hole locations separated by some broad subtle rolls.  The strategy for the approach shot will be decidedly different from day to day based on the hole location on the green.</p>

