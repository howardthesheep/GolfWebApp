	      	<p>On a golf course with many gorgeous settings, the sixth hole could be the prettiest of all.  This is the longest hole on the golf course playing to a par 5 and a length of 662 yards from the tournament tees.  The tees sit on a high ridge with the player looking down through the first landing area, across a valley and up in to the second landing area set into another ridge that helps turn the hole softly from right to left.  The green rests next to a small pond.  It's a wonderful setting for a very challenging golf hole.  Players are given the opportunity to bomb away off the tee box as there is no real trouble in the landing area except for the longest hitters, who might need to keep in mind the creek that bisects the fairway.  From the first landing area the golf hole really starts to get interesting.  The longer the player hits it off the tee, the tougher the decision gets on the second shot, as the bunkers that are set in to the hillside on the left-hand side of the landing area come in to play.  The small pot bunker in the middle of the fairway at about the 537 yard mark off the tournament tee will be a key feature for players to consider as they decide whether to lay up, go over it trying to get as close to the green as possible, or play to either side of it.  The green is relatively small, but also relatively flat with very little contour within the surface.  The green sits hard against the ponds' edge on the left-hand side with some fairway bailout available on the right-hand side of the green.  The 6th hole is a true three shot par 5 that will require players to plan their shots and execute their plan if they are to be successful.</p>

