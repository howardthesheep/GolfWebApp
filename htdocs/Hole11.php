	      	<p>The 11th hole is a long par 4 that will require two well played shots for anyone hoping to make par.   The tee shot plays uphill to one of the widest landing areas on the golf course.  The landing area is deceptive though, as the view and angle of approach to the green is much better from one side of the landing area than the other.  The landing area is bisected by one pot bunker, creating a left and right option for the majority of players.  The right-hand fairway is slightly elevated, requiring a stronger tee shot.  The reward for finding this part of the fairway is a full view of the green and a good angle of approach.  The green is fronted by a single bunker and sits atop a small knob amongst a stand of trees.</p>

