	      	<p>The back nine at Old Hawthorne begins with a relatively short but very challenging par 5.  The tee shot on this hole could be the most intimidating shot on the golf course.  It plays from an elevated tee down to a fairway that slopes hard from left to right, with the entire right-hand side of the fairway through the first landing area guarded by a large lake.  A well played tee shot can carry the ridge running across the fairway, giving the golfers a little added distance as their shot runs down the slope.  Once in the landing area they will encounter a number of options that they need to consider for their second shot.  The green sits on the far side of Grindstone Creek and there is fairway on both side of the creek.  A large tree also divides the fairway on the far side and some greenside and fairway bunkering further defines the players options.  The green is reachable in two but the orientation of the green and the contours within the surface will require a player to hit a precise second shot if they expect to have any hope of eagle or birdie.  Players not playing for the green in two still have three or four options with which to choose from in deciding whether or not to challenge the creek on their second shot or play across the creek with a short third shot to the well-protected green.  Like most of the holes on this course, hole 10 will require golfers to match their chosen strategy to their ability to execute that strategy.</p>

