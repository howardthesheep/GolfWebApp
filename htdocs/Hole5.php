	      	<p>Upon leaving the fourth green, the players will come up a slight hill and through a strand of trees  opening up onto the fifth tee.  From here they will get their first look at the hole.  It is a short risk/reward par 4 that doglegs left around a large lake on the left-hand side of the hole.  The hole sits beautifully within a natural valley.  Two bunkers located out in the landing area create a variety of options for the player on this cape style hole.  Down the center line of the hole, it plays to a yardage of 393 yards from the tournament tees, but the strong dogleg actually gives players an opportunity to go for the green in one if they feel that they can carry the entire length of the pond with their tee shot.  For everyone else there are still two or three different ways in which the hole can be successfully navigated.  An aggressive play off the tee will bring the pot bunker in the middle of the fairway into play.  A more conservative play off the tee to the wide part of the fairway will leave a longer approach shot into the green, though still only a mid to short iron. The green surface itself is rather large and undulating with a strong ridge running into the center of the green from the back side, splitting the green into two halves.  The hole location on any given day will help dictate the strategy that players find most successful on this hole.</p>

